#!/usr/bin/env node
/**
 * token-packager.js — 设计 Token → npm 包发布器
 *
 * 从 DESIGN-TOKENS.md 生成可发布的 npm 包（package.json + CSS + Tailwind + JSON）。
 * 跨项目引用: npm install @feicai/design-tokens → import '@feicai/design-tokens/tokens.css'
 *
 * 用法:
 *   node token-packager.js                          # 生成 npm 包到 .claude/token-pkg/
 *   node token-packager.js --publish                # 生成 + npm publish
 *   node token-packager.js --version 2.0.0          # 指定版本
 *   node token-packager.js --scope @my-org          # 自定义 scope
 *
 * 通用性: 任何有 DESIGN-TOKENS.md 的产品 → npm 包。
 */

const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

const PROJECT_ROOT = process.env.CLAUDE_PROJECT_DIR || process.cwd();
const PKG_DIR = path.join(PROJECT_ROOT, '.claude', 'token-pkg');

function extractProductInfo() {
  const tokensPath = path.join(PROJECT_ROOT, 'DESIGN-TOKENS.md');
  if (!fs.existsSync(tokensPath)) return { name: 'design-tokens', version: '1.0.0' };

  const content = fs.readFileSync(tokensPath, 'utf-8');
  const nameMatch = content.match(/Design Tokens\s*[·]\s*(.+)/);
  const productName = nameMatch ? nameMatch[1].trim().toLowerCase().replace(/\s+/g, '-') : 'design-tokens';

  return { name: `@feicai/${productName}`, version: '1.0.0', sourceName: productName };
}

function generatePackage(scope, version) {
  const info = extractProductInfo();
  const pkgName = scope || info.name;
  const pkgVersion = version || info.version;

  if (!fs.existsSync(PKG_DIR)) fs.mkdirSync(PKG_DIR, { recursive: true });

  // 复制 DESIGN-TOKENS.md
  const tokensPath = path.join(PROJECT_ROOT, 'DESIGN-TOKENS.md');
  if (fs.existsSync(tokensPath)) {
    fs.copyFileSync(tokensPath, path.join(PKG_DIR, 'README.md'));
  }

  // 复制生成的 tokens.css
  const cssSrc = path.join(PROJECT_ROOT, 'do-zhou', 'src', 'styles', 'tokens.css');
  if (fs.existsSync(cssSrc)) {
    fs.copyFileSync(cssSrc, path.join(PKG_DIR, 'tokens.css'));
  }

  // 生成 tokens.json (机器可读)
  const tokenMapPath = path.join(PROJECT_ROOT, '.claude', '.token-map.json');
  if (fs.existsSync(tokenMapPath)) {
    fs.copyFileSync(tokenMapPath, path.join(PKG_DIR, 'tokens.json'));
  }

  // 生成 Tailwind 预设
  const tailwindPreset = `// @feicai/${info.sourceName} Tailwind Preset
// 自动生成于 ${new Date().toISOString()}

module.exports = {
  theme: {
    extend: {
      colors: require('./tokens.json'),
    },
  },
};
`;
  fs.writeFileSync(path.join(PKG_DIR, 'tailwind-preset.js'), tailwindPreset);

  // 生成 package.json
  const pkgJson = {
    name: pkgName,
    version: pkgVersion,
    description: `Design tokens for ${info.sourceName} — Auto-generated by FeiCai Pipeline`,
    main: 'tokens.css',
    files: ['tokens.css', 'tokens.json', 'tailwind-preset.js', 'README.md'],
    keywords: ['design-tokens', 'css', 'tailwind', 'feicai'],
    license: 'UNLICENSED',
    _note: '此包由 token-packager.js 自动生成。来源: DESIGN-TOKENS.md',
  };
  fs.writeFileSync(path.join(PKG_DIR, 'package.json'), JSON.stringify(pkgJson, null, 2));

  // 生成 .npmignore
  fs.writeFileSync(path.join(PKG_DIR, '.npmignore'), 'node_modules/\n.claude/\nsrc/\n');

  console.log(`📦 Token 包已生成: ${PKG_DIR}`);
  console.log(`   名称: ${pkgName}@${pkgVersion}`);
  console.log(`   文件: tokens.css + tokens.json + tailwind-preset.js + README.md`);
  console.log(`\n发布:`);
  console.log(`   cd ${PKG_DIR} && npm publish`);
  console.log(`\n使用:`);
  console.log(`   npm install ${pkgName}`);
  console.log(`   import '${pkgName}/tokens.css'  // CSS 变量`);
  console.log(`   const tokens = require('${pkgName}/tokens.json')  // 机器可读`);
  return PKG_DIR;
}

function main() {
  const args = process.argv.slice(2);
  const scopeIdx = args.indexOf('--scope');
  const versionIdx = args.indexOf('--version');
  const publish = args.includes('--publish');

  const scope = scopeIdx >= 0 ? args[scopeIdx + 1] : null;
  const version = versionIdx >= 0 ? args[versionIdx + 1] : null;

  const pkgDir = generatePackage(scope, version);

  if (publish) {
    console.log('\n📡 发布到 npm...');
    execSync('npm publish', { cwd: pkgDir, stdio: 'inherit' });
    console.log('✅ 已发布');
  }
}

module.exports = { generatePackage };

if (require.main === module) {
  main();
}
