#!/usr/bin/env node
/**
 * spec-to-unit-test.js — 源码 → 单元测试自动生成器
 *
 * 读服务/工具源文件 → 检测依赖模式 → 自动生成 vitest 测试骨架。
 * 对标 spec-to-e2e（Spec → Playwright），这个是 Code → vitest。
 *
 * 生成规则:
 *   class 导出        → new 实例 + 方法测试
 *   export function   → 函数测试
 *   import electron   → vi.mock('electron')
 *   import fs/path    → 临时目录 fixture
 *   import fetch      → vi.stubGlobal('fetch')
 *
 * 用法:
 *   node spec-to-unit-test.js                          # 扫描 src/ 生成测试
 *   node spec-to-unit-test.js --file <path>            # 单文件
 *   node spec-to-unit-test.js --dry-run                # 仅报告，不写文件
 *   node spec-to-unit-test.js --dir src/main/services  # 指定目录
 *
 * 通用性: 读 .ts 源码，AST 级检测依赖，不绑定任何产品。
 */

const fs = require('fs');
const path = require('path');

const PROJECT_ROOT = process.env.CLAUDE_PROJECT_DIR || process.cwd();

// ── 依赖检测 ──

function detectDependencies(content) {
  return {
    hasElectron: /from\s+['"]electron['"]/.test(content) || /require\s*\(\s*['"]electron['"]/.test(content),
    hasFs: /from\s+['"]fs['"]/.test(content) || /require\s*\(\s*['"]fs['"]/.test(content),
    hasPath: /from\s+['"]path['"]/.test(content) || /require\s*\(\s*['"]path['"]/.test(content),
    hasFetch: /\bfetch\b/.test(content),
    hasReact: /from\s+['"]react['"]/.test(content) || /React/.test(content),
  };
}

function detectExports(content) {
  const exports = [];
  // export class ClassName
  const classRx = /export\s+class\s+(\w+)/g;
  let m;
  while ((m = classRx.exec(content)) !== null) {
    exports.push({ type: 'class', name: m[1] });
  }
  // export function fnName / export async function fnName
  const fnRx = /export\s+(?:async\s+)?function\s+(\w+)/g;
  while ((m = fnRx.exec(content)) !== null) {
    if (!['default'].includes(m[1]) && !m[1].startsWith('_')) {
      exports.push({ type: 'function', name: m[1] });
    }
  }
  // export const objName = { ... }
  const objRx = /export\s+const\s+(\w+)\s*[:=]\s*\{/g;
  while ((m = objRx.exec(content)) !== null) {
    exports.push({ type: 'object', name: m[1] });
  }
  return exports;
}

function detectConstructorParams(content) {
  const ctorRx = /constructor\s*\(\s*(\w+)\s*:\s*\w+/g;
  let m;
  const params = [];
  while ((m = ctorRx.exec(content)) !== null) {
    params.push(m[1]);
  }
  return params;
}

// ── 测试生成 ──

function generateTest(filePath, content) {
  const deps = detectDependencies(content);
  const exports = detectExports(content);
  const ctorParams = detectConstructorParams(content);

  if (exports.length === 0) return null;

  const srcDir = path.join(PROJECT_ROOT, 'do-zhou', 'src');
  const relToSrc = path.relative(path.dirname(filePath), filePath).replace(/\\/g, '/').replace(/\.ts$/, '');
  const importPath = './' + path.basename(filePath).replace(/\.ts$/, '');

  let code = `${deps.hasReact ? '// @vitest-environment jsdom\n' : ''}/**
 * AUTO-GENERATED by spec-to-unit-test.js
 * 来源: ${relPath}
 */
import { describe, it, expect${deps.hasElectron || deps.hasFetch || deps.hasReact ? ', vi' : ''}${deps.hasFs ? ', beforeEach, afterEach' : ''} } from 'vitest'
`;

  // 依赖 mock
  if (deps.hasElectron) {
    code += `
vi.mock('electron', () => ({
  app: { getPath: vi.fn(() => '/tmp/test') },
  safeStorage: { isEncryptionAvailable: vi.fn(() => false), encryptString: vi.fn((s) => Buffer.from(s)), decryptString: vi.fn((b) => b.toString()) },
}))
`;
  }
  if (deps.hasFetch) {
    code += `vi.stubGlobal('fetch', vi.fn())\n`;
  }
  if (deps.hasFs) {
    code += `
let tmpDir: string
beforeEach(() => { tmpDir = require('path').join(require('os').tmpdir(), 'test-' + Date.now()); require('fs').mkdirSync(tmpDir, { recursive: true }) })
afterEach(() => { try { require('fs').rmSync(tmpDir, { recursive: true, force: true }) } catch(_) {} })
`;
  }

  code += `
import { ${exports.map(e => e.name).join(', ')} } from '${importPath}'

describe('${exports[0].name}', () => {
`;

  for (const exp of exports) {
    if (exp.type === 'class') {
      const paramStr = ctorParams.length > 0 ? ctorParams.map(p => `'/tmp/test-${p}'`).join(', ') : '';
      code += `  const ${exp.name.charAt(0).toLowerCase() + exp.name.slice(1)} = new ${exp.name}(${paramStr})\n\n`;
      code += `  it('构造成功', () => {\n`;
      code += `    expect(${exp.name.charAt(0).toLowerCase() + exp.name.slice(1)}).toBeDefined()\n`;
      code += `  })\n`;
    } else if (exp.type === 'function') {
      code += `  it('${exp.name} → 可调用', async () => {\n`;
      code += `    // TODO: 根据实际业务逻辑补充参数和断言\n`;
      code += `    expect(typeof ${exp.name}).toBe('function')\n`;
      code += `  })\n`;
    } else if (exp.type === 'object') {
      code += `  it('${exp.name} → 对象已导出', () => {\n`;
      code += `    expect(${exp.name}).toBeDefined()\n`;
      code += `    expect(typeof ${exp.name}).toBe('object')\n`;
      code += `  })\n`;
    }
  }

  code += `})\n`;
  return code;
}

// ── 文件扫描 ──

function findSourceFiles(srcDir) {
  const files = [];
  function walk(dir, depth) {
    if (depth > 6) return;
    try {
      for (const e of fs.readdirSync(dir, { withFileTypes: true })) {
        if (e.name.startsWith('.') || e.name === 'node_modules' || e.name === '__tests__') continue;
        const full = path.join(dir, e.name);
        if (e.isDirectory()) walk(full, depth + 1);
        else if (e.name.endsWith('.ts') && !e.name.includes('.test.') && !e.name.includes('.spec.') && !e.name.endsWith('.d.ts')) {
          files.push(full);
        }
      }
    } catch (_) {}
  }
  walk(srcDir, 0);
  return files;
}

// ── 主入口 ──

function main() {
  const args = process.argv.slice(2);
  const fileIdx = args.indexOf('--file');
  const dirIdx = args.indexOf('--dir');
  const dryRun = args.includes('--dry-run');

  let files = [];
  if (fileIdx >= 0) {
    files = [path.resolve(args[fileIdx + 1])];
  } else {
    const srcDir = dirIdx >= 0 ? path.resolve(args[dirIdx + 1]) : path.join(PROJECT_ROOT, 'do-zhou', 'src', 'main', 'services');
    files = findSourceFiles(srcDir);
  }

  let generated = 0;
  for (const file of files) {
    const content = fs.readFileSync(file, 'utf-8');
    const testCode = generateTest(file, content);
    if (!testCode) continue;

    const testFile = file.replace(/\.ts$/, '.test.ts');
    if (fs.existsSync(testFile)) continue; // 跳过已有测试

    if (dryRun) {
      console.log(`[DRY-RUN] 会生成: ${path.relative(PROJECT_ROOT, testFile)}`);
      generated++;
      continue;
    }

    fs.writeFileSync(testFile, testCode);
    console.log(`✅ ${path.relative(PROJECT_ROOT, testFile)}`);
    generated++;
  }

  console.log(`\n📊 ${dryRun ? '[DRY-RUN] ' : ''}生成 ${generated} 个测试文件`);
  if (generated > 0 && !dryRun) {
    console.log('运行 npx vitest run --coverage 验证覆盖率提升');
  }
}

module.exports = { generateTest, detectDependencies, detectExports };

if (require.main === module) {
  main();
}
